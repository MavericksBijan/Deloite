package com.deloitte;

public class BusJourney {
	
	private int customerId;
	private String journey_start_date_time;
	private String routeName;
	
	
	
	
	
	
	
	
	
	public int getCustomerId() {
		return customerId;
	}
	public void setCustomerId(int customerId) {
		this.customerId = customerId;
	}
	public String getJourney_start_date_time() {
		return journey_start_date_time;
	}
	public void setJourney_start_date_time(String journey_start_date_time) {
		this.journey_start_date_time = journey_start_date_time;
	}
	public String getRouteName() {
		return routeName;
	}
	public void setRouteName(String routeName) {
		this.routeName = routeName;
	}
	
	
	
	
	
	

}
